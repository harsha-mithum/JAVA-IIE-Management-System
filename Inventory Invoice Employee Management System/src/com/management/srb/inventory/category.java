    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
  if (CatID.getText().isEmpty())
        {
            CatID.setBackground(new java.awt.Color(255,0,51));
            JOptionPane.showMessageDialog(this,"CatID field is empty !!");
        }
  else if (CatName.getText().isEmpty())
        {
            CatName.setBackground(new java.awt.Color(255,0,51));
            JOptionPane.showMessageDialog(this,"CatName field is empty !!");
        }
      
     
         else
        {
            int opt =JOptionPane.showConfirmDialog(null,"Are you sure to add this record!!","Add Record",JOptionPane.YES_NO_OPTION);
            if (opt==0)

            {
        
        
        try {
            Con = DriverManager.getConnection(dburl, dbuname, dbpass);
            PreparedStatement add = Con.prepareStatement("Insert into category values(?,?)");
            add.setInt(1, Integer.valueOf(CatID.getText()));
            add.setString(2, CatName.getText());

            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Category Successfully Added");
             CatID.setText("");
       CatName.setText("");
            Con.close();
            SelectCat();
          setView();
        } catch (SQLException e) {
            e.printStackTrace();
        }
               }
               }
    }//GEN-LAST:event_AddBtnMouseClicked

