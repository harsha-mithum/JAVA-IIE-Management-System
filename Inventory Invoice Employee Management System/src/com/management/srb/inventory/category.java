     public void SelectCat() {
        try {
            Con = DriverManager.getConnection(dburl, dbuname, dbpass);
            St = Con.createStatement();
            Rs = St.executeQuery("select * from category");
            CategoryTable.setModel(DbUtils.resultSetToTableModel(Rs));
            CategoryTable.getTableHeader().setFont(new Font("Segoe UI",Font.BOLD,14));
            CategoryTable.getTableHeader().setOpaque(false);
            CategoryTable.getTableHeader().setBackground(new Color(32,136,203));
            CategoryTable.getTableHeader().setForeground(new Color(0,0,0));
            CategoryTable.setRowHeight(25);
        } catch (SQLException e) {
        }
    }
    
    public void setView(){
        try {
            PreparedStatement stmtMaximumID = Con.prepareStatement("SELECT MAX(id) from category");
            ResultSet getMaxValueID = stmtMaximumID.executeQuery();

            while (getMaxValueID.next()) {
                this.CatID.setText(Integer.toString(getMaxValueID.getInt(1)+1));
            }

        } catch (SQLException e) {
        }
    }
