    private void txtsupid1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsupid1KeyReleased
        txtsupid1.setBackground(new Color(255, 255, 255));
        if (evt.getKeyCode() == KeyEvent.VK_ENTER)
        try {

            String sql1 = "select * from suppliers where supplierid = ?";
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/sr_bio_foods", "root", "");
            pst = (PreparedStatement) con.prepareStatement(sql1);
            String suid = txtsupid2.getText();
            pst.setString(1, suid);
            rs = (ResultSet) pst.executeQuery();
            if (rs.next()) {

                String supname = rs.getString("supplier_name");
                txtsupname2.setText(supname.trim());
                String addr = rs.getString("address");
                txtcusaddre2.setText(addr.trim());
                String product = rs.getString("products");
                txtproduct2.setText(product.trim());
                String coin = rs.getString("contactinfo");
                txtcontinfo2.setText(coin.trim());
            }

        } catch (SQLException ex) {
            Logger.getLogger(supplier.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_txtsupid1KeyReleased

