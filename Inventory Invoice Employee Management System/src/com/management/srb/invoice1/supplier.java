    private void btnAddCusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCusActionPerformed
        if (txtsuppid.getText().isEmpty()) {
            txtsuppid.setBackground(new Color(255, 0, 51));
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else if (txtcusaddre.getText().isEmpty()) {
            txtcusaddre.setBackground(new Color(255, 0, 51));
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else if (txtproduct.getText().isEmpty()) {
            txtproduct.setBackground(new Color(255, 0, 51));
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else if (txtcontinfo.getText().isEmpty()) {
            txtcontinfo.setBackground(new Color(255, 0, 51));
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            int opt = JOptionPane.showConfirmDialog(null, "Are you sure to add this record!!", "Add Record", JOptionPane.YES_NO_OPTION);
            if (opt == 0) {
                try {
                    String query = "insert into suppliers "
                            + "(supplier_name,supplierid,address,products,contactinfo)"
                            + "values (?,?,?,?,?)";

                    con = DriverManager.getConnection("jdbc:mysql://localhost/sr_bio_foods", "root", "");
                    pst = con.prepareStatement(query);

                    pst.setString(1, txtsuppname.getText());
                    pst.setString(2, txtsuppid.getText());
                    pst.setString(3, txtcusaddre.getText());
                    pst.setString(4, txtproduct.getText());
                    pst.setString(5, txtcontinfo.getText());
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Add Successfull");
                    txtsuppid.setText("");
                    txtsuppname.setText("");
                    txtcusaddre.setText("");
                    txtproduct.setText("");
                    txtcontinfo.setText("");
                    autoidgen();
                } catch (SQLException ex) {
                    Logger.getLogger(supplier.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        showtable();
    }//GEN-LAST:event_btnAddCusActionPerformed

