    public void autoidgen() {
        try {

            Statement st = con.createStatement();
            rs = st.executeQuery("select Max(supplierid) from suppliers");
            rs.next();
            rs.getString("Max(supplierid)");
            if (rs.getString("Max(supplierid)") == null) {
                txtsuppid.setText("SUP0001");
            } else {
                long id = Long.parseLong(rs.getString("Max(supplierid)").substring(4, rs.getString("Max(supplierid)").length()));
                id++;
                txtsuppid.setText("SUP" + String.format("%04d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(supplier.class.getName()).log(Level.SEVERE, null, ex);

        }

    }

    public void showtable() {
        try {

            Statement st = con.createStatement();
            String sql = "select * from suppliers";
            rs = st.executeQuery(sql);
            while (rs.next()) {
                String supname = rs.getString("supplier_name");
                String supid = rs.getString("supplierid");
                String add = rs.getString("address");
                String product = rs.getString("products");
                String contacde = rs.getString("contactinfo");

                String custb[] = {supname, supid, add, product, contacde};
                DefaultTableModel tbmodel = (DefaultTableModel) suppliershowtable.getModel();
                tbmodel.addRow(custb);

                suppliershowtable.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
                suppliershowtable.getTableHeader().setOpaque(false);
                suppliershowtable.getTableHeader().setBackground(new Color(32, 136, 203));
                suppliershowtable.getTableHeader().setForeground(new Color(0, 0, 0));
                suppliershowtable.setRowHeight(25);

            }
            con.close();

        } catch (SQLException ex) {
            Logger.getLogger(supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

